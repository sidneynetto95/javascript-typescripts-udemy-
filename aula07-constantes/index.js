// Não podemos criar constantes com palavras reservadas
// Constantes precisam ter nomes significativos
// Não pode começar com o nome de uma constante com um número
// Não podem conter espaços ou traços
// Utilizamos camelCase(iniciamos as palavras com letras minúsculas e as próximas com letra MAIÚSCULA)
// Case-sensitive 
// Não podemos modificar o valor de um constante
// NÃO UTILIZE VAR, UTILIZE CONST.

const primeiroNumero = 5;
const segundoNumero = 10;
const resultado = primeiroNumero * segundoNumero;
const resultadoDuplicado = resultado * 2;

console.log(resultado);
console.log(resultadoDuplicado);

